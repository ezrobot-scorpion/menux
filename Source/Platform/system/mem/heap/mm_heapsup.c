#include <string.h>
#include <stdint.h>
#include <stdbool.h>
#include <menux/nuxos.h>
#include <menux/nuxos/mem/heapimpl.h>

/*!
********************************************************************************
* LOCAL VARIABLES
*******************************************************************************/
static OS_MUTEX gx_mmheapmutex;

/*!
********************************************************************************
* PUBLIC FUNCTIONS
*******************************************************************************/
/*!
********************************************************************************
* @brief :
*
* @param :
*
* @retval:
*******************************************************************************/
void mm_heap_mutexinit(void)
{
	OS_ERR error;

	OSMutexCreate(&gx_mmheapmutex, "mem mutex", &error);
}

/*!
********************************************************************************
* @brief :
*
* @param :
*
* @retval:
*******************************************************************************/
void mm_heap_mutexwaitone(void)
{
	OS_ERR error;

	OSMutexPend(&gx_mmheapmutex, 0, OS_OPT_PEND_BLOCKING, DEF_NULL, &error);
}

/*!
********************************************************************************
* @brief :
*
* @param :
*
* @retval:
*******************************************************************************/
void mm_heap_mutexrelease(void)
{
	OS_ERR error;

	OSMutexPost(&gx_mmheapmutex, OS_OPT_POST_NONE, &error);
}
