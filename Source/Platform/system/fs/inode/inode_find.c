#include <menux/nuxos/fs/vfs.h>
#include <menux/nuxos/fs/inode.h>

/*!
********************************************************************************
* PUBLIC FUNCTIONS
*******************************************************************************/
/*!
********************************************************************************
* @brief :
*
* @param :
*
* @retval:
*******************************************************************************/
int inode_find(rt_inode_search_t *desc)
{
	int ret;

	inode_wait_mutex();
	ret = inode_search(desc);
	if (ret >= 0)
	{
		rt_inode_t *node = desc->node;
		node->i_crefs++;
	}

	inode_release_mutex();

	return ret;
}
