#include <menux/nuxos/fs/vfs.h>
#include <menux/nuxos/fs/inode.h>
#include <menux/nuxos/mem/heap.h>

/*!
********************************************************************************
* PUBLIC FUNCTIONS
*******************************************************************************/
/*!
********************************************************************************
* @brief :
*
* @param :
*
* @retval:
*******************************************************************************/
void inode_free(rt_inode_t *node)
{
	if (node != NULL)
	{
		inode_free(node->i_peer);
		inode_free(node->i_child);
		os_heap_free(node);
	}
}
